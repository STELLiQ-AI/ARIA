def load_qmsum(max_examples: int) -> list:
    """QMSum — query-based meeting summaries."""
    print("  Loading QMSum...")
    try:
        ds = load_dataset("pszemraj/qmsum-cleaned", split="train")
    except Exception:
        try:
            ds = load_dataset("mattercalm/qmsum", split="train")
        except Exception as e:
            print(f"  WARNING: Could not load QMSum: {e}")
            return []

    results = []
    for item in ds:
        # QMSum has meeting_transcripts or input, and answer or output
        transcript = item.get("meeting_transcripts") or item.get("input", "")
        summary = item.get("answer") or item.get("output", "")
        if transcript and summary and len(transcript) > 200:
            results.append((transcript[:MAX_TRANSCRIPT_CHARS], summary, "retrospective"))
        if len(results) >= max_examples:
            break
    print(f"  -> {len(results)} examples from QMSum")
    return results


DATASET_LOADERS = {
    "ami": load_ami,
    "dialogsum": load_dialogsum,
    "samsum": load_samsum,
    "qmsum": load_qmsum,
}


# ---------------------------------------------------------------------------
# Re-summarization: Convert free-text summary → ARIA 5-field JSON
# ---------------------------------------------------------------------------

def make_aria_json_from_summary(original_summary: str, template: str) -> str:
    """
    Convert a free-text summary into ARIA's 6-field JSON format.
    Uses heuristic splitting when no API key is available,
    or Claude API for high-quality conversion.
    """
    return _heuristic_split(original_summary, template)


def _heuristic_split(summary: str, template: str) -> str:
    """Split a free-text summary into ARIA's 5 fields using sentence-level heuristics."""
    sentences = [s.strip() for s in summary.replace("\n", " ").split(". ") if s.strip()]
    if not sentences:
        sentences = [summary.strip()]

    # Add periods back
    sentences = [s if s.endswith(".") else s + "." for s in sentences]

    n = len(sentences)

    if n == 1:
        return json.dumps({
            "title": _extract_title(summary),
            "what_was_planned": sentences[0],
            "what_happened": sentences[0],
            "why_it_happened": "Context discussed during the conversation.",
            "how_to_improve": "Follow up on discussed items.",
            "ai_perspective": "This brief discussion would benefit from more structured follow-up to ensure key points are captured and actionable next steps are clearly defined.",
        })

    if n == 2:
        return json.dumps({
            "title": _extract_title(summary),
            "what_was_planned": sentences[0],
            "what_happened": sentences[1],
            "why_it_happened": "Discussed during the meeting.",
            "how_to_improve": "Follow up on action items.",
            "ai_perspective": "The conversation covers the basics but may benefit from deeper exploration of root causes and more specific action items with clear ownership.",
        })

    # 3+ sentences: distribute across fields
    chunk = max(1, n // 5)
    return json.dumps({
        "title": _extract_title(summary),
        "what_was_planned": " ".join(sentences[:chunk]),
        "what_happened": " ".join(sentences[chunk:chunk*2]),
        "why_it_happened": " ".join(sentences[chunk*2:chunk*3]),
        "how_to_improve": " ".join(sentences[chunk*3:chunk*4]),
        "ai_perspective": " ".join(sentences[chunk*4:]) if chunk*4 < n else "The discussion demonstrates engagement with the topic but would benefit from examining underlying patterns and potential blind spots that may not have been addressed directly.",
    })


def _extract_title(text: str) -> str:
    """Extract a short title from summary text."""
    words = text.split()[:10]
    title = " ".join(words).rstrip(".,;:!?")
    if len(title) > 60:
        title = title[:57] + "..."
    return title.title()


def resummarize_with_claude(transcript: str, original_summary: str,
                             template: str, client) -> str:
    """Use Claude API to produce a high-quality ARIA-format JSON summary (6-field schema)."""
    system_prompt = SYSTEM_PROMPTS[template]

    # Provide the original summary as context for Claude
    user_msg = (
        f"Summarize this meeting transcript:\n\n{transcript}\n\n"
        f"---\nReference summary (for context, rewrite in the required JSON format): "
        f"{original_summary}"
    )

    try:
        response = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=1024,  # Increased for 6-field schema with ai_perspective
            system=system_prompt,
            messages=[{"role": "user", "content": user_msg}],
        )
        return response.content[0].text.strip()
    except Exception as e:
        print(f"    Claude API error: {e}")
        return make_aria_json_from_summary(original_summary, template)


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------

def convert_to_training_format(transcript: str, summary_json: str,
                                template: str) -> dict:
    """Create a single training example in ARIA's chat format."""
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPTS[template]},
            {"role": "user", "content": f"Summarize this meeting transcript:\n\n{transcript}"},
            {"role": "assistant", "content": summary_json},
        ]
    }


def validate_json_output(text: str) -> bool:
    """Check if the assistant output is valid ARIA JSON (6-field schema)."""
    try:
        obj = json.loads(text)
        return all(k in obj for k in ["title", "what_was_planned", "what_happened",
                                       "why_it_happened", "how_to_improve",
                                       "ai_perspective"])
    except (json.JSONDecodeError, TypeError):
        return False


def main():
    parser = argparse.ArgumentParser(description="Download real-world datasets for ARIA training")
    parser.add_argument("--output", default="data/real_world_data.jsonl",
                        help="Output JSONL file path")
    parser.add_argument("--datasets", default="all",
                        help="Comma-separated dataset names, or 'all' (default)")
    parser.add_argument("--max-per-dataset", type=int, default=500,
                        help="Max examples per dataset (default: 500)")
    parser.add_argument("--use-claude", action="store_true",
                        help="Use Claude API for high-quality re-summarization")
    parser.add_argument("--heuristic-only", action="store_true",
                        help="Use only heuristic splitting (no API, faster)")
    args = parser.parse_args()

    # Determine which datasets to load
    if args.datasets == "all":
        dataset_names = list(DATASET_LOADERS.keys())
    else:
        dataset_names = [d.strip() for d in args.datasets.split(",")]
        for name in dataset_names:
            if name not in DATASET_LOADERS:
                print(f"ERROR: Unknown dataset '{name}'. Available: {list(DATASET_LOADERS.keys())}")
                sys.exit(1)

    # Setup Claude client if requested
    claude_client = None
    if args.use_claude and not args.heuristic_only:
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        if api_key and anthropic:
            claude_client = anthropic.Anthropic(api_key=api_key)
            print("Using Claude API for re-summarization (higher quality, costs ~$0.01/example)")
        else:
            print("WARNING: ANTHROPIC_API_KEY not set or anthropic not installed. Using heuristic mode.")

    # Load all datasets
    print(f"\n{'='*60}")
    print(f"ARIA Training Data — Real-World Dataset Downloader")
    print(f"{'='*60}")
    print(f"Datasets: {', '.join(dataset_names)}")
    print(f"Max per dataset: {args.max_per_dataset}")
    print(f"Mode: {'Claude API' if claude_client else 'Heuristic'}")
    print(f"Output: {args.output}")
    print(f"{'='*60}\n")

    all_raw = []
    for name in dataset_names:
        loader = DATASET_LOADERS[name]
        examples = loader(args.max_per_dataset)
        all_raw.extend(examples)

    if not all_raw:
        print("\nERROR: No examples loaded from any dataset.")
        sys.exit(1)

    print(f"\nTotal raw examples: {len(all_raw)}")
    print("Converting to ARIA training format...\n")

    # Convert to training format
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    valid_count = 0
    skipped_count = 0

    with open(output_path, "w", encoding="utf-8") as f:
        for transcript, original_summary, template in tqdm(all_raw, desc="Converting"):
            # Generate ARIA-format JSON summary
            if claude_client:
                summary_json = resummarize_with_claude(
                    transcript, original_summary, template, claude_client
                )
                # Rate limit: ~40 req/min on free tier
                time.sleep(0.1)
            else:
                summary_json = make_aria_json_from_summary(original_summary, template)

            # Validate
            if not validate_json_output(summary_json):
                skipped_count += 1
                continue

            # Write training example
            example = convert_to_training_format(transcript, summary_json, template)
            f.write(json.dumps(example, ensure_ascii=False) + "\n")
            valid_count += 1

    print(f"\n{'='*60}")
    print(f"Done! Wrote {valid_count} training examples to {args.output}")
    print(f"Skipped {skipped_count} examples (invalid JSON output)")
    print(f"{'='*60}")

    # Template distribution
    template_counts = {}
    for _, _, template in all_raw:
        template_counts[template] = template_counts.get(template, 0) + 1
    print("\nTemplate distribution:")
    for t, c in sorted(template_counts.items()):
        print(f"  {t}: {c}")


if __name__ == "__main__":
    main()
