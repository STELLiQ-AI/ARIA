#!/usr/bin/env python3
"""
download_datasets.py — Download real-world meeting/dialogue datasets from
Hugging Face and convert them to ARIA's training format.

Each output example follows the Llama 3.1 Instruct chat format:
{
    "messages": [
        {"role": "system", "content": "<template system prompt>"},
        {"role": "user", "content": "Summarize this meeting transcript:\n\n<transcript>"},
        {"role": "assistant", "content": "<JSON summary>"}
    ]
}

The assistant response is generated by re-summarizing the original dataset's
summary into ARIA's 6-field JSON schema (includes ai_perspective). For datasets
that already have structured summaries (SOAP, legal), the fields are mapped
directly with ai_perspective generated via Claude API.

Usage:
    python scripts/download_datasets.py --output data/real_world_data.jsonl
    python scripts/download_datasets.py --datasets ami,dialogsum --output data/subset.jsonl
    python scripts/download_datasets.py --max-per-dataset 100 --output data/small.jsonl

Requires: pip install datasets anthropic tqdm
Requires: ANTHROPIC_API_KEY env var (for re-summarization into ARIA JSON format)
"""
import argparse
import json
import os
import sys
import time
from pathlib import Path

try:
    from datasets import load_dataset
except ImportError:
    print("ERROR: 'datasets' package not installed. Run: pip install datasets")
    sys.exit(1)

try:
    import anthropic
except ImportError:
    anthropic = None

from tqdm import tqdm


# ---------------------------------------------------------------------------
# ARIA system prompts — 6-field schema with ai_perspective
# (Must match generate_data.py prompts for training consistency)
# ---------------------------------------------------------------------------

SYSTEM_PROMPTS = {
    "retrospective": (
        "You are an expert meeting analysis assistant with deep domain knowledge. "
        "Your task is to analyze meeting or recording transcripts and produce a structured "
        "summary with genuine analytical insight.\n\n"
        "Generate a JSON object with exactly these six fields:\n"
        "{\n"
        '  "title": "<A concise, descriptive title in Title Case>",\n'
        '  "what_was_planned": "<The stated purpose, goals, agenda, or objectives. What were participants trying to accomplish?>",\n'
        '  "what_happened": "<Key events, decisions, outcomes, and turning points. What actually occurred vs. what was planned?>",\n'
        '  "why_it_happened": "<Root causes, contributing factors, constraints, and context. What drove the outcomes — both stated and unstated?>",\n'
        '  "how_to_improve": "<Specific, actionable recommendations with owners and timelines where possible. What concrete steps should be taken?>",\n'
        '  "ai_perspective": "<Your independent analytical assessment. Identify patterns, risks, blind spots, or strategic insights the participants may not have explicitly recognized. Connect observations to broader principles, flag potential second-order effects, and offer a candid evaluation of what was discussed vs. what was avoided.>"\n'
        "}\n\n"
        "Rules:\n"
        "- Title: Title Case, specific and descriptive (not generic).\n"
        "- Each field should be 2-5 sentences with substantive content, not filler.\n"
        "- what_happened should contrast plan vs. reality when applicable.\n"
        "- why_it_happened should identify root causes, not just restate symptoms.\n"
        "- how_to_improve must include specific actions, not vague aspirations.\n"
        "- ai_perspective must add genuine analytical value — do NOT restate other fields.\n"
        "- Attribute points to specific speakers when the transcript identifies them.\n"
        "- Output ONLY the JSON object, no additional text."
    ),
    "simple": (
        "You are a clear, insightful meeting summarizer. Analyze transcripts and produce "
        "structured summaries with useful analytical perspective.\n\n"
        "Generate a JSON object with exactly these six fields:\n"
        "{\n"
        '  "title": "<A concise, descriptive title in Title Case>",\n'
        '  "what_was_planned": "<What was the purpose of this conversation or meeting?>",\n'
        '  "what_happened": "<What were the main topics discussed and key takeaways?>",\n'
        '  "why_it_happened": "<What important context or background was mentioned?>",\n'
        '  "how_to_improve": "<What are the action items and next steps?>",\n'
        '  "ai_perspective": "<Your honest assessment: what went well in this discussion, what was left unresolved, any concerns or opportunities the group may not have fully addressed, and whether the action items are likely to be effective.>"\n'
        "}\n\n"
        "Rules:\n"
        "- Use plain, everyday language. Be direct and helpful.\n"
        "- Each field should be 1-3 sentences. Be concise but substantive.\n"
        "- ai_perspective should add practical insight, not just summarize.\n"
        "- Output ONLY the JSON object, no additional text."
    ),
}

# Max transcript length (matches ARIA's MAX_TRANSCRIPT_CHARS = 13600)
MAX_TRANSCRIPT_CHARS = 12000  # Leave room for prompt overhead


# ---------------------------------------------------------------------------
# Dataset loaders — each returns list of (transcript, original_summary, template)
# ---------------------------------------------------------------------------

def load_ami(max_examples: int) -> list:
    """AMI Meeting Corpus — product design meetings."""
    print("  Loading AMI corpus (knkarthick/AMI)...")
    try:
        ds = load_dataset("knkarthick/AMI", split="train")
    except Exception as e:
        print(f"  WARNING: Could not load AMI: {e}")
        return []

    results = []
    for item in ds:
        dialogue = item.get("dialogue", "")
        summary = item.get("summary", "")
        if dialogue and summary and len(dialogue) > 100:
            results.append((dialogue[:MAX_TRANSCRIPT_CHARS], summary, "retrospective"))
        if len(results) >= max_examples:
            break
    print(f"  -> {len(results)} examples from AMI")
    return results


def load_dialogsum(max_examples: int) -> list:
    """DialogSum — real-life scenario dialogues."""
    print("  Loading DialogSum (knkarthick/dialogsum)...")
    try:
        ds = load_dataset("knkarthick/dialogsum", split="train")
    except Exception as e:
        print(f"  WARNING: Could not load DialogSum: {e}")
        return []

    results = []
    for item in ds:
        dialogue = item.get("dialogue", "")
        summary = item.get("summary", "")
        if dialogue and summary and len(dialogue) > 100:
            results.append((dialogue[:MAX_TRANSCRIPT_CHARS], summary, "simple"))
        if len(results) >= max_examples:
            break
    print(f"  -> {len(results)} examples from DialogSum")
    return results


def load_samsum(max_examples: int) -> list:
    """SAMSum — messenger conversations. NC license."""
    print("  Loading SAMSum (Samsung/samsum)...")
    try:
        ds = load_dataset("Samsung/samsum", split="train")
    except Exception as e:
        print(f"  WARNING: Could not load SAMSum: {e}")
        return []

    results = []
    for item in ds:
        dialogue = item.get("dialogue", "")
        summary = item.get("summary", "")
        if dialogue and summary and len(dialogue) > 80:
            results.append((dialogue[:MAX_TRANSCRIPT_CHARS], summary, "simple"))
